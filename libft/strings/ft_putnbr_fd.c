#include "libft.h"

int	ft_putnbr_fd(int n, int fd)
{
	char	c;

	if (n < 0 && n > -10)
		write(fd, "-", 1);
	if (n < 0)
		c = ((10 - (n % 10)) % 10) + '0';
	else
		c = (n % 10) + '0';
	n = n / 10;
	if (n != 0)
		ft_putnbr_fd(n, fd);
	return (write(fd, &c, 1));
}
